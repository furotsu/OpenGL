GLuint createCircle(GLfloat posX, GLfloat posY, GLfloat radius, const ShaderProgram& program)
{
    // Creating vertices array
	GLint sidesAmount = 100;
	const GLuint verticesAmount = sidesAmount + 2;
	const GLfloat doublePi = M_PI * 2;

	GLfloat vertices[310];
	vertices[0] = posX;
	vertices[1] = posY;
	vertices[2] = 0.0f;

	for (GLint i = 1; i != verticesAmount; ++i)
	{
		vertices[i * 3] =   posX + 0.5 * sin(i * doublePi / sidesAmount);
		vertices[i*3 + 1] = posY + 0.5 * cos(i * doublePi / sidesAmount);
		vertices[i*3 + 2] = 0.0f;
	}

    // OpenGL managment
	GLuint VAO, VBO;

	glGenVertexArrays(1, &VAO);
	glGenBuffers(1, &VBO);

	glBindVertexArray(VAO);
	glBindBuffer(GL_ARRAY_BUFFER, VBO);
	glBufferData(GL_ARRAY_BUFFER, sizeof(vertices), vertices, GL_STATIC_DRAW);

	glVertexAttribPointer(1, 3, GL_FLOAT, GL_FALSE, 3 * sizeof(GL_FLOAT), (void*)0);
	glEnableVertexAttribArray(1);

	return VAO;
}

